public class Contoh1 {
    public static void main(String[] args) {
        
        //pengulangan for
        int x;
        for(x = 1; x <= 5; x++)
        {
            System.out.println("SMK TELKOM MALANG");
        }
        //pengulangan while
        int i = 1;
        while(i <= 5)
        {
            System.out.println("SMK TELKOM MALANG");
            i++;
        }
        //pengulangan DO WHILE
        int j = 1;
        do 
            
        {
            System.out.println("SMK TELKOM MALANG");
            j++;
        }
        while(j <= 5);
    }
}
